function defaultValues() {
  return {
    moisture: 42,
    ph: 6.7,
    ec: 1.1,
    temperature: 27,
    nitrogen: 70,
    phosphorus: 35,
    potassium: 55,
    rainfall: state.config?.districts?.find((item) => item.name.en === state.district)?.avg_rainfall || 720,
  }
}

function randomBetween(min, max, decimals = 0) {
  return Number((Math.random() * (max - min) + min).toFixed(decimals))
}

function sampleValues() {
  const rainfallBase = state.config?.districts?.find((item) => item.name.en === state.district)?.avg_rainfall || 720
  return {
    moisture: randomBetween(18, 82, 0),
    ph: randomBetween(5.2, 8.2, 1),
    ec: randomBetween(0.4, 2.8, 1),
    temperature: randomBetween(20, 35, 0),
    nitrogen: randomBetween(15, 120, 0),
    phosphorus: randomBetween(8, 62, 0),
    potassium: randomBetween(18, 95, 0),
    rainfall: randomBetween(Math.max(250, rainfallBase - 220), Math.min(1600, rainfallBase + 320), 0),
  }
}

function pickText(value) {
  if (!value) return "--"
  if (typeof value === "string") return value
  return value[state.language] || value.en || "--"
}

function cropName(crop) {
  return crop ? crop[state.language] || crop.en : "--"
}

function loadHistory() {
  return JSON.parse(localStorage.getItem("ssi_history") || "[]")
}

function saveHistory(entry) {
  const history = loadHistory()
  history.unshift(entry)
  localStorage.setItem("ssi_history", JSON.stringify(history.slice(0, 8)))
}

function refreshLiveBannerText() {
  if (state.liveStatus === "configured") {
    liveBannerTitleNode.textContent = t("liveConnectedTitle")
    liveBannerTextNode.textContent = t("liveConnectedText")
    return
  }

  if (state.liveStatus === "error") {
    liveBannerTitleNode.textContent = t("liveErrorTitle")
    liveBannerTextNode.textContent = state.liveError ? `${t("liveErrorText")} ${state.liveError}` : t("liveErrorText")
    return
  }

  liveBannerTitleNode.textContent = t("liveFallbackTitle")
  liveBannerTextNode.textContent = t("liveFallbackText")
}

async function fetchLiveReading() {
  try {
    const response = await fetch("/api/live-reading")
    const result = await response.json()
    if (!response.ok) throw new Error(result.error || "Adafruit IO request failed")

    state.liveStatus = result.configured ? "configured" : "fallback"
    state.liveError = ""
    state.liveValues = result.values
    writeValues(state.liveValues)
  } catch (error) {
    state.liveStatus = "error"
    state.liveError = error.message
    state.liveValues = sampleValues()
    writeValues(state.liveValues)
  } finally {
    refreshLiveBannerText()
  }
}

function applyTranslations() {
  document.documentElement.lang = state.language
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = t(node.dataset.i18n)
  })

  document.querySelectorAll("[data-language]").forEach((button) => {
    button.classList.toggle("active", button.dataset.language === state.language)
  })

  refreshLiveBannerText()
  renderSamples()
  renderHistory()
  if (state.lastResult) {
    renderResult(state.lastResult)
  } else {
    healthPill.textContent = t("waiting")
  }
}

function populateDistricts() {
  districtSelect.innerHTML = state.config.districts
    .map((item) => `<option value="${item.name.en}">${item.name[state.language] || item.name.en}</option>`)
    .join("")
  districtSelect.value = state.district
  updateDistrictNote()
}

function populateCrops() {
  cropSelect.innerHTML = state.config.crops
    .map((crop) => `<option value="${crop.en}">${crop[state.language] || crop.en}</option>`)
    .join("")

  if (!state.selectedCrop) {
    state.selectedCrop = state.config.crops[0]?.en || "Rice"
  }
  cropSelect.value = state.selectedCrop
}

