function setChoice(name, value) {
  state[name] = value
  document.querySelectorAll(`[data-choice='${name}']`).forEach((button) => {
    button.classList.toggle("active", button.dataset.value === value)
  })
  updateUIState()
}

function updateUIState() {
  manualCropBlock.classList.toggle("hidden", state.operationMode !== "manual")
  multiTools.classList.toggle("hidden", state.sampleMode !== "multiple")
  liveBanner.classList.toggle("hidden", state.dataMode !== "live")
  updateTopStrip()

  if (state.dataMode === "live") {
    startLiveMode()
  } else {
    stopLiveMode()
  }
}

function readValues() {
  const values = {}
  fields.forEach((field) => {
    values[field.key] = Number(document.querySelector(`#${field.key}`).value)
  })
  return values
}

function writeValues(values) {
  fields.forEach((field) => {
    document.querySelector(`#${field.key}`).value = values[field.key]
    document.querySelector(`#${field.key}Range`).value = values[field.key]
  })
  drawChart(values)
}

function startLiveMode() {
  stopLiveMode()
  fetchLiveReading()
  state.liveTimer = window.setInterval(fetchLiveReading, 5000)
}

function stopLiveMode() {
  if (state.liveTimer) {
    window.clearInterval(state.liveTimer)
    state.liveTimer = null
  }
}

function addCurrentSample() {
  const values = state.dataMode === "live" ? state.liveValues : readValues()
  state.samples.push({ ...values })
  renderSamples()
}

function renderSamples() {
  if (!state.samples.length) {
    sampleList.innerHTML = `<div class="sample-item"><strong>${t("addSamplesPrompt")}</strong></div>`
    return
  }

  sampleList.innerHTML = state.samples
    .map((sample, index) => {
      return `
        <div class="sample-item">
          <strong>${t("addedPoint")} ${index + 1}</strong>
          <p>M ${sample.moisture}% | pH ${sample.ph} | EC ${sample.ec} | NPK ${sample.nitrogen}/${sample.phosphorus}/${sample.potassium} | R ${sample.rainfall} mm</p>
        </div>
      `
    })
    .join("")
}

function buildPayload() {
  const currentValues = state.dataMode === "live" ? state.liveValues : readValues()
  const payload = {
    ...currentValues,
    language: state.language,
    district: state.district,
    planting_mode: state.plantingMode,
    operation_mode: state.operationMode,
    data_mode: state.dataMode,
    sample_mode: state.sampleMode,
    analysis_type: state.analysisType,
  }

  if (state.operationMode === "manual") {
    payload.crop = state.selectedCrop
  }

  if (state.sampleMode === "multiple") {
    payload.samples = (state.samples.length ? state.samples : [currentValues]).map((sample) => ({ ...sample }))
  }

  return payload
}

function healthClass(status) {
  if (status === "High") return "high"
  if (status === "Medium") return "medium"
  return "low"
}

function makeListItem(title, body, level = "info") {
  return `<div class="list-item ${level ? `level-${level}` : ""}"><strong>${title}</strong><p>${body}</p></div>`
}

function renderResult(result) {
  state.lastResult = result
  const soil = result.soil_health
  const crop = result.crop_recommendation
  const fert = result.fertilizer_recommendation

  healthPill.textContent = state.language === "kn" ? soil.status_kn : soil.status
  healthPill.className = `health-pill ${healthClass(soil.status)}`
  heroScore.textContent = soil.score
  selectionSummary.textContent = pickText(result.selection_summary)
  farmerMessage.textContent = pickText(result.farmer_message)

  soilHealthText.textContent = pickText(soil.summary)
  keyIssueText.textContent = pickText(soil.key_issue)

  const cropSource = crop.source === "manual" ? t("cropSourceManual") : t("cropSourceAi")
  const alternatives = crop.alternatives.length
    ? `${t("alternatives")}: ${crop.alternatives.map((item) => cropName(item)).join(", ")}`
    : ""
  const confidenceText = crop.confidence != null ? `${t("confidence")}: ${Math.round(crop.confidence * 100)}%` : ""

  cropText.textContent = cropName(crop.selected)
  cropSubtext.textContent = [cropSource, confidenceText, alternatives].filter(Boolean).join(" | ") || "--"
  irrigationText.textContent = pickText(result.irrigation_advice.message)
  incomeText.textContent = pickText(result.income_improvement_suggestion)

  fertilizerNote.textContent = pickText(fert.note)
  ureaBadge.textContent = `${t("urea")}: ${fert.fertilizers_kg_ha.urea} kg/ha`
  dapBadge.textContent = `${t("dap")}: ${fert.fertilizers_kg_ha.dap} kg/ha`
  mopBadge.textContent = `${t("mop")}: ${fert.fertilizers_kg_ha.mop} kg/ha`

  nutrientPlan.innerHTML = Object.entries(fert.nutrients)
    .map(([nutrient, values]) => {
      const labelKey = `${nutrient}Label`
      return `
        <div class="list-item">
          <strong>${t(labelKey)}</strong>
          <p>${t("requirement")}: ${values.required} kg/ha | ${t("available")}: ${values.available} kg/ha | ${t("gap")}: ${values.gap} kg/ha</p>
        </div>
      `
    })
    .join("")

  alertsList.innerHTML = result.alerts
    .map((alert) => makeListItem(pickText(alert.message), pickText(alert.message), alert.level))
    .join("")

  deficiencyList.innerHTML = result.deficiency_detection
    .map((item) => makeListItem(item.nutrient, pickText(item.message), "info"))
    .join("")

  weatherRotation.innerHTML = [
    makeListItem(t("weather"), pickText(result.weather_note), "info"),
    makeListItem(t("rotation"), pickText(result.crop_rotation_suggestion), "info"),
  ].join("")

  explanationsList.innerHTML = result.explanations
    .map((item) => makeListItem(pickText(item), pickText(item), "info"))
    .join("")

  if (result.zones?.length) {
    zoneList.innerHTML = result.zones
      .map((zone) => {
        return `
          <div class="zone-item">
            <strong>${t("addedPoint")} ${zone.zone} | ${state.language === "kn" ? (zone.soil_health_status === "High" ? "ಹೆಚ್ಚು" : zone.soil_health_status === "Medium" ? "ಮಧ್ಯಮ" : "ಕಡಿಮೆ") : zone.soil_health_status} | ${cropName(zone.crop)}</strong>
            <p>${pickText(zone.key_issue)}</p>
          </div>
        `
      })
      .join("")
  } else {
    zoneList.innerHTML = `<div class="list-item"><strong>${t("zoneEmpty")}</strong></div>`
  }

  drawChart(result.average_values)
  renderHistory()
}

function renderHistory() {
  const history = loadHistory()
  if (!history.length) {
    historyList.innerHTML = `<div class="list-item"><strong>${t("historyEmpty")}</strong></div>`
    return
  }

  historyList.innerHTML = history
    .map((item) => {
      const cropLabel = state.language === "kn" ? item.cropKn : item.crop
      const districtLabel = state.language === "kn" ? item.districtKn : item.district
      return `
        <div class="list-item">
          <strong>${districtLabel} | ${cropLabel}</strong>
          <p>${item.time} | ${item.status} | ${item.score}/100</p>
        </div>
      `
    })
    .join("")
}

async function analyzeSoil() {
  analyzeBtn.disabled = true
  analyzeBtn.textContent = t("analyzing")

  try {
    const response = await fetch("/api/predict", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify(buildPayload()),
    })

    const result = await response.json()
    if (!response.ok) throw new Error(result.error || "Prediction failed")

    saveHistory({
      time: new Date().toLocaleString(),
      district: result.district.en,
      districtKn: result.district.kn,
      crop: result.crop_recommendation.selected.en,
      cropKn: result.crop_recommendation.selected.kn,
      score: result.soil_health.score,
      status: state.language === "kn" ? result.soil_health.status_kn : result.soil_health.status,
    })

    renderResult(result)
  } catch (error) {
    healthPill.textContent = "Error"
    healthPill.className = "health-pill low"
    soilHealthText.textContent = error.message
  } finally {
    analyzeBtn.disabled = false
    analyzeBtn.textContent = t("analyzeButton")
  }
}

function drawChart(values) {
  const ctx = chartCanvas.getContext("2d")
  const width = chartCanvas.width = chartCanvas.clientWidth
  const height = chartCanvas.height = 240
  const padding = 34
  const gap = 12
  const barWidth = (width - padding * 2 - gap * (fields.length - 1)) / fields.length

  ctx.clearRect(0, 0, width, height)
  ctx.fillStyle = "#fffaf3"
  ctx.fillRect(0, 0, width, height)
  ctx.strokeStyle = "rgba(92, 70, 44, 0.14)"
  ctx.beginPath()
  ctx.moveTo(padding, height - padding)
  ctx.lineTo(width - padding / 2, height - padding)
  ctx.stroke()

  const palette = ["#2f7b4d", "#29678d", "#b26a27", "#9f3d33"]

  fields.forEach((field, index) => {
    const value = values[field.key]
    const percent = Math.max(0, Math.min(100, ((value - field.min) / (field.max - field.min)) * 100))
    const barHeight = Math.max(8, (percent / 100) * (height - padding * 2))
    const x = padding + index * (barWidth + gap)
    const y = height - padding - barHeight

    ctx.fillStyle = palette[index % palette.length]
    ctx.fillRect(x, y, barWidth, barHeight)
    ctx.fillStyle = "#4a3b2d"
    ctx.font = "11px Trebuchet MS"
    ctx.textAlign = "center"
    ctx.fillText(`${Math.round(percent)}%`, x + barWidth / 2, y - 8)
    ctx.fillStyle = "#6b5c4f"
    ctx.fillText(field.key.slice(0, 3).toUpperCase(), x + barWidth / 2, height - 10)
  })
}

async function loadConfig() {
  const response = await fetch("/api/config")
  state.config = await response.json()
  state.liveStatus = state.config.adafruit?.configured ? "configured" : "fallback"
  populateDistricts()
  populateCrops()
  const defaults = defaultValues()
  writeValues(defaults)
}

function wireInputs() {
  fields.forEach((field) => {
    const numberInput = document.querySelector(`#${field.key}`)
    const rangeInput = document.querySelector(`#${field.key}Range`)

    numberInput.addEventListener("input", () => {
      rangeInput.value = numberInput.value
      drawChart(readValues())
    })

    rangeInput.addEventListener("input", () => {
      numberInput.value = rangeInput.value
      drawChart(readValues())
    })
  })
}

function initEvents() {
  document.querySelectorAll("[data-language]").forEach((button) => {
    button.addEventListener("click", () => {
      state.language = button.dataset.language
      localStorage.setItem("ssi_language", state.language)
      populateDistricts()
      populateCrops()
      applyTranslations()
      updateTopStrip()
    })
  })

  districtSelect.addEventListener("change", () => {
    state.district = districtSelect.value
    localStorage.setItem("ssi_district", state.district)
    updateDistrictNote()
    const values = readValues()
    values.rainfall = state.config.districts.find((item) => item.name.en === state.district)?.avg_rainfall || values.rainfall
    writeValues(values)
    updateTopStrip()
  })

  cropSelect.addEventListener("change", () => {
    state.selectedCrop = cropSelect.value
    updateTopStrip()
  })

  document.querySelectorAll("[data-choice]").forEach((button) => {
    button.addEventListener("click", () => setChoice(button.dataset.choice, button.dataset.value))
  })

  startDashboardBtn.addEventListener("click", showDashboard)
  changeSetupBtn.addEventListener("click", showOnboarding)
  generateDemoBtn.addEventListener("click", () => {
    if (state.dataMode === "live") {
      fetchLiveReading()
      return
    }

    const values = sampleValues()
    writeValues(values)
  })
  addSampleBtn.addEventListener("click", addCurrentSample)
  clearSamplesBtn.addEventListener("click", () => {
    state.samples = []
    renderSamples()
  })
  soilForm.addEventListener("submit", (event) => {
    event.preventDefault()
    analyzeSoil()
  })
}

